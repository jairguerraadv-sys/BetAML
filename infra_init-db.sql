-- ===================== VIEWS FOR QUERY EFFICIENCY =====================

CREATE VIEW active_rules_by_tenant AS
SELECT 
    r.id,
    r.tenant_id,
    r.name,
    r.condition_dsl,
    r.severity,
    r.category,
    t.name as tenant_name
FROM rule_definitions r
JOIN tenants t ON r.tenant_id = t.id
WHERE r.status = 'ACTIVE';

CREATE VIEW recent_alerts AS
SELECT 
    a.id,
    a.tenant_id,
    a.player_id,
    a.severity,
    a.status,
    a.alert_type,
    a.created_at,
    t.name as tenant_name,
    COUNT(*) OVER (PARTITION BY a.player_id) as player_alert_count
FROM alerts a
JOIN tenants t ON a.tenant_id = t.id
WHERE a.created_at > CURRENT_TIMESTAMP - INTERVAL '30 days'
ORDER BY a.created_at DESC;

